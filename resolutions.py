RESOLUTIONS = []
for label, w, h in _RAW:
    dw, dh = w / SCALE_FACTOR, h / SCALE_FACTOR
    rem_w, rem_h = dw / 16, dh / 16
    RESOLUTIONS.append({
        "label": label, "width": w, "height": h,
        "design_w": dw, "design_h": dh,
        "rem_w": rem_w, "rem_h": rem_h,
        "orientation": "portrait" if h > w else ("landscape" if w > h else "square"),
        "max_cols": 1 if rem_w < 30 else (2 if rem_w < 40 else 3),
        "allow_sidebar": rem_w >= 42,  # 仅在非常宽时允许侧边栏
    })

BREAKPOINTS = {"sm": 3200, "md": 4000, "lg": 4800, "xl": 5600}

def pick_resolution(name=None):
    if name:
        for r in RESOLUTIONS:
            if r["label"] == name:
                return r
    return random.choice(RESOLUTIONS)